<?php
function findOriginalNumber($n) {
    // Перебираем все возможные значения c (цифра единиц)
    for ($c = 1; $c <= 9; $c++) { // c не может быть 0
        // Вычисляем оставшуюся часть
        $remaining = $n - 100 * $c;

        // Проверяем, что оставшаяся часть является двузначным числом
        if ($remaining >= 10 && $remaining < 100) {
            // Извлекаем a и b
            $a = $remaining % 10; // последняя цифра (единицы)
            $b = (int)($remaining / 10); // первая цифра (десятки)

            // Проверяем, что a и b - допустимые цифры
            if ($a >= 0 && $a <= 9 && $b >= 0 && $b <= 9) {
                // Формируем оригинальное число x
                $x = 100 * $b + 10 * $a + $c;
                return $x;
            }
        }
    }
    return null; // если не нашли подходящее число
}

// Ввод значения n с клавиатуры
$n = (int)readline("Введите число n (1 ≤ n ≤ 999, единицы != 0): ");

// Проверяем границы n
if ($n < 1 || $n > 999 || $n % 10 == 0) {
    echo "Ошибка: число должно быть в диапазоне от 1 до 999 и его единицы не могут быть нулем.\n";
} else {
    $originalNumber = findOriginalNumber($n);

    if ($originalNumber !== null) {
        echo "Оригинальное число x: " . $originalNumber . "\n";
    } else {
        echo "Оригинальное число x не найдено.\n";
    }
}
?>